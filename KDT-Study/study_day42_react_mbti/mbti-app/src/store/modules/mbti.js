/* ì•¡ì…˜ type ì„¤ì • */
const NEXT = 'mbti/NEXT';
const CHECK = 'mbti/CHECK';
const RESET = 'mbti/RESET';

/* ì•¡ì…˜ ìƒì„± í•¨ìˆ˜ */
// payload -> ì„ íƒì— ë‹¤ë¥¸ ê²°ê³¼ ê°’ result ì „ë‹¬ í•„ìš”

export function next() {
  return {
    type: NEXT,
  };
}

export function check(result) {
  return {
    type: CHECK,
    payload: { result },
  };
}

export function reset() {
  return {
    type: RESET,
  };
}

/* Reducer */
export default function mbti(state = initState, action) {
  switch (action.type) {
    case CHECK:
      return {
        ...state,
        mbtiResult: state.mbtiResult + action.payload.result,
      };
    case NEXT:
      return {
        ...state,
        page: state.page + 1,
      };
    case RESET:
      return {
        ...state,
        page: 0,
        mbtiResult: '',
      };
    default:
      return state;
  }
}
